package com.example.demo.model;

public class Teacher {

    private Integer id;


    private String realname;


    private String name;

   
    private Integer gradeid;

   
    private Integer subjectid;

  
    private String phone;

    
    private Integer price;

  
    private Integer techYear;

    
    private Integer techTime;

  
    private String image;

  
    public Integer getId() {
        return id;
    }

   
    public void setId(Integer id) {
        this.id = id;
    }

   
    public String getRealname() {
        return realname;
    }

   
    public void setRealname(String realname) {
        this.realname = realname;
    }

    
    public String getName() {
        return name;
    }

   
    public void setName(String name) {
        this.name = name;
    }

    
    public Integer getGradeid() {
        return gradeid;
    }

    
    public void setGradeid(Integer gradeid) {
        this.gradeid = gradeid;
    }

    
    public Integer getSubjectid() {
        return subjectid;
    }

  
    public void setSubjectid(Integer subjectid) {
        this.subjectid = subjectid;
    }

   
    public String getPhone() {
        return phone;
    }

    
    public void setPhone(String phone) {
        this.phone = phone;
    }


    public Integer getPrice() {
        return price;
    }


    public void setPrice(Integer price) {
        this.price = price;
    }


    public Integer getTechYear() {
        return techYear;
    }

   
    public void setTechYear(Integer techYear) {
        this.techYear = techYear;
    }

  
    public Integer getTechTime() {
        return techTime;
    }


    public void setTechTime(Integer techTime) {
        this.techTime = techTime;
    }

    
    public String getImage() {
        return image;
    }

    
    public void setImage(String image) {
        this.image = image;
    }
}